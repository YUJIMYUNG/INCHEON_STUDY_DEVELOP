// 수리 연산자 [+, -, *, /, %]

const a = 1 + 1; // 2[더하기]
const a1 = 1 - 2; // -1 [빼기]
const a2 = 2 * 2; // 4 [곱하기]
const a3 = 2 ** 3; // 8 [제곱]
const a4 = 3 / 2; // 1.5[ 나누기]
const a5 = 3 % 2; // 1 [나머지]

// 문자열 연결 연산자, 부호 반대 연산자
const b1 = 1 + "2"; // 12 [문자 연결]
const b2 = -100; // -100 [부호 반대]

// 대입 연산자
const c = "마라탕";

// 비교 연산자 [<, >, <=, >=, ==, ===, !=] -> boolean 타입
const d = 3 > 0; // true
const d1 = 3 < 5; // true
const d2 = 3 >= 0; // true
const d3 = 3 <= 5; // true
const d4 = 10 == 11; // false;
const d5 = 10 === 10; // true; [타입이 같나? 여부도 확인함]
const d6 = 3 != 1; // true;
